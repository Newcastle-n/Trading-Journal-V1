def h(level: int, text: str, anchor: str | None = None):
    a = anchor or "".join(c if c.isalnum() or c in "-_" else "-" for c in text.lower())
    return {"type": "heading", "level": level, "text": text, "id": a}


def p(text: str):
    return {"type": "paragraph", "text": text}


def ul(items: list[str]):
    return {"type": "list", "ordered": False, "items": items}


def ol(items: list[str]):
    return {"type": "list", "ordered": True, "items": items}


def callout(variant: str, title: str, text: str):
    return {"type": "callout", "variant": variant, "title": title, "text": text}


def quote(text: str, cite: str = ""):
    return {"type": "quote", "text": text, "cite": cite}


def table(headers: list[str], rows: list[list[str]]):
    return {"type": "table", "headers": headers, "rows": rows}


def checklist(items: list[str]):
    return {"type": "checklist", "items": [{"text": t, "checked": False} for t in items]}


def accordion(title: str, blocks: list):
    return {"type": "accordion", "title": title, "blocks": blocks}


